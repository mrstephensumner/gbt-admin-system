// Generates worker/db/seed-perf.sql: 5,000 talent records for the SC-003
// performance check. Deterministic (seeded PRNG) so runs are comparable.
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const COUNT = 5000
const FIRST = ['James','Sophie','Raj','Amelia','Tom','Priya','Marcus','Grace','Oliver','Hannah','Ben','Chloe','Idris','Victoria','Sam','Nina','George','Fatima','Leila','Danny','Ruth','Callum','Aisha','Peter','Megan','Yusuf','Ella','Noah','Freya','Arjun']
const LAST = ['Patel','Clarke','Okafor','Nguyen','Webb','Sharma','Hughes','Adeyemi','Fraser','Doyle','Whitfield','Haddad','Carver','Barnes','Mensah','Lane','Porter','Kowalski','Ellison','Begum','Armstrong','Bishop','Chandler','Drummond','Eastwood','Farrell','Gibson','Holloway','Ibrahim','Jennings']
const CITIES = ['London','Manchester','Leeds','Bristol','Cardiff','Edinburgh','Glasgow','Birmingham','Liverpool','Newcastle','Sheffield','Oxford','Cambridge','York','Bath']
const STATUSES = ['available','available','available','available','on_hold','booked','confirmed','cancelled']
const TOPICS = 12 // matches seed-dev topic ids 1..12

let state = 20260716
const rand = () => {
  state = (state * 1664525 + 1013904223) % 4294967296
  return state / 4294967296
}
const pick = (arr) => arr[Math.floor(rand() * arr.length)]

const lines = [
  '-- Generated by generate-perf-seed.mjs — 5,000 records for SC-003. Run after db:migrate:local.',
  'DELETE FROM publication;',
  'DELETE FROM change_record;',
  'DELETE FROM talent_topic;',
  'DELETE FROM talent_photo;',
  'DELETE FROM talent;',
  'DELETE FROM topic;',
  'DELETE FROM brand;',
  "INSERT INTO brand (id, slug, name, created_at) VALUES (1, 'great-british-speakers', 'Great British Speakers', '2026-07-16T09:00:00Z');",
]
lines.push(
  `INSERT INTO topic (id, name, created_at, created_by) VALUES ${Array.from(
    { length: TOPICS },
    (_, i) => `(${i + 1}, 'Topic ${i + 1}', '2026-07-16T09:00:00Z', 'seed')`,
  ).join(', ')};`,
)

const talentRows = []
const topicRows = []
for (let i = 1; i <= COUNT; i++) {
  const ref = `TAL-${String(i).padStart(4, '0')}`
  const name = `${pick(FIRST)} ${pick(LAST)}`
  const rate = rand() < 0.1 ? 'NULL' : String(Math.round((500 + rand() * 14500)) * 100)
  const status = pick(STATUSES)
  const archived = rand() < 0.03 ? "'2026-07-01T00:00:00Z'" : 'NULL'
  talentRows.push(
    `(${i}, '${ref}', '${name.replace(/'/g, "''")}', 'Speaker ${i}', 'Biography for ${name.replace(/'/g, "''")} (${ref}).', ${rate}, '${pick(CITIES)}, UK', NULL, NULL, '${status}', ${archived}, 1, '2026-07-16T09:00:00Z', 'seed', '2026-07-16T09:00:00Z', 'seed')`,
  )
  const t1 = 1 + Math.floor(rand() * TOPICS)
  topicRows.push(`(${i}, ${t1})`)
  const t2 = 1 + Math.floor(rand() * TOPICS)
  if (t2 !== t1) topicRows.push(`(${i}, ${t2})`)
}

const CHUNK = 500
for (let i = 0; i < talentRows.length; i += CHUNK) {
  lines.push(
    `INSERT INTO talent (id, reference, name, headline, biography, day_rate_pence, location, email, phone, status, archived_at, version, created_at, created_by, updated_at, updated_by) VALUES ${talentRows.slice(i, i + CHUNK).join(', ')};`,
  )
}
for (let i = 0; i < topicRows.length; i += CHUNK) {
  lines.push(`INSERT INTO talent_topic (talent_id, topic_id) VALUES ${topicRows.slice(i, i + CHUNK).join(', ')};`)
}
lines.push(`UPDATE ref_counter SET next_number = ${COUNT + 1} WHERE id = 1;`)

const out = path.join(path.dirname(fileURLToPath(import.meta.url)), 'seed-perf.sql')
writeFileSync(out, lines.join('\n') + '\n')
console.log(`Wrote ${out} (${COUNT} records)`)
